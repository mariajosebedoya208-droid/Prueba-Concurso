#Importaci√≥n las librer√≠as

import streamlit as st
import pandas as pd
import numpy as np
import yfinance as yf
import matplotlib.pyplot as plt
import warnings
from io import BytesIO
from reportlab.lib.pagesizes import letter
from reportlab.lib import colors
